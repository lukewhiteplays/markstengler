			<h3>The History of HCG for Weight Loss</h3>
			The use of HCG and a low calorie diet for weight loss has an interesting history. It starts with a British born medical doctor by the name of A.T.W. Simeons. Trained at a German medical school, he practiced in India from 1931 until 1949, working on conventional treatments for the testing and treatment of malaria. For his research against malaria, Dr. Simeons was awarded an “Order of Merit” by the Red Cross. In 1949, he transitioned to the Salvador Mundi International Hospital in Rome, where he focused on the treatment of obesity.
			
