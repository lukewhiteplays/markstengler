				<p class="lead">Prescription medications for <strong>Hypothyroidism</strong> are very common in the United States. According to the <a href="#">American Association of Clinical Endocrinologists</a> studies have shown that as many as 10 percent of women and 3 percent of men have hypothyroidism (low thyroid activity). The thyroid gland is located at the base of your neck below your Adam’s apple. It secretes thyroid hormones that have pronounced effects on the cells of your body.
				
				<br><br>The two main thyroid hormones include <strong>T3 (liothyronine)</strong> and <strong>T4 ( L-thyroxine)</strong>. Another thyroid hormone known as thyroid stimulating hormone (TSH) regulates the secretion of T3 and T4. This hormone is secreted by the pituitary gland when it senses blood levels of thyroid hormones are getting low. It also receives messages from the brain (hypothalamus) that influences TSH secretion. When the hypothalamus senses blood levels of thyroid hormones it signals the pituitary gland to release more TSH. It is secreted by the pituitary and then signals the thyroid gland to release more of T4 and T3 hormone.
	
				<br><br>Thyroid hormones control the metabolic activity in every cell. This is important for temperature control, weight regulation, heart rate, and energy production. Thyroid activity even influences one’s mood and neurotransmitter balance and affects the balance of other hormones in the body. Hypothyroidism occurs when the thyroid gland is under active. This leads to a shortage of thyroid hormones. The most common cause is a disease known as Hashimoto’s thyroiditis.This is an autoimmune condition where the body’s immune system produces antibodies to the thyroid gland. This attack on the thyroid gland leads to the suppression of thyroid hormone production and secretion. Other reasons for hypothyroidism may include iodine and other nutritional deficiencies, stress, pregnancy, medications such as lithium or estrogen therapy, and an under functioning pituitary gland.
	
				<br><br>Hypothyroidism is most common in middle-aged and older women. It can occur at any age though, including infants and teenager. <em>Untreated hypothyroidism can be life threatening.</em></p>
